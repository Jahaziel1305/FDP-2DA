public class Acumuladores {
    public static void main(String[] args) {
        // ACUMULADORES:
        // += -+ *= /=
        int cuenta = 0;
        // ASIGNACION ---> =
        cuenta = cuenta + 100; // ACUMULADOR
        System.out.println(cuenta);
        cuenta = cuenta + 1000;
        System.out.println(cuenta);
        cuenta = cuenta - 800;
        System.out.println(cuenta);
        cuenta += 2000;
        System.out.println(cuenta);
    }
}