public class While {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 10) { // se va a repetir si la condicion es verdadera
            System.out.println("Jahaziel Ortiz");
            i++;
        }
        System.out.println("");
        System.out.println("CICLO DO WHILE");
        System.out.println("");
        i = 1;
        do {
            System.out.println("Jahaziel Ortiz");
            i++;
        } while (i <= 10);
    }
}