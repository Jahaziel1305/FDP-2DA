package EVA2_8_FOR;

public class For {
    public static void main(String[] args) {
        // 3 parametros
        // variable y valor de inicio
        // hasta donde llegas
        // incremento o decremento
        // i++ significa sumarle un valor de la i
        for (int i = 1; i <= 10; i++) {
            System.out.println("Hola mundo!");
        }
        for (int i = 10; i >= 1; i--) {
            System.out.println(i);
        }
    }
}